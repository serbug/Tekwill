public class Ex1_3 {
    public static void main(String[] args) {
        /* 3. From 0 to 5
           Write a program that prints numbers from 0 to 5 inclusive.
           Each number must be on a new line.
         */

        for(int i=0;i<=5;i++){
            System.out.println("\t\t\t"+i);
        }

    }
}
