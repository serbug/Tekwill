public class Triangle extends Shape {

    protected double a;
    protected double b;
    protected double c;

    public Triangle() {
    }

    public Triangle(double a, double b, double c) {
        this.a = a;
        this.b = b;
        this.c = c;
    }

    @Override
    double getPerimeter() {
        return a + b + c;
    }

    @Override
    double getArea() {
        return getPerimeter() / 2;
    }
}
